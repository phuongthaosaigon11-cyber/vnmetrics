function parseEthData(text) {
    const lines = text.split('\n').filter(l => l.trim());
    const rows = [];
    const headers = ["Date", "ETHA", "FETH", "ETHW", "TETH", "ETHV", "QETH", "EZET", "ETHE", "ETH", "Total"];

    for (const line of lines) {
        if (line.includes('Total') || line.includes('Fee') || line.includes('Seed')) continue;

        // Regex t√¨m Ng√†y Th√°ng NƒÉm (VD: 23 Jul 2024)
        const dateMatch = line.match(/^(\d{2}\s+[A-Za-z]{3}\s+\d{4})/);
        if (!dateMatch) continue;

        const dateStr = dateMatch[1];
        let remaining = line.replace(dateStr, '').trim();
        const parts = remaining.split(/[\t\s]+/);

        // ETH c√≥ kho·∫£ng 10 c·ªôt s·ªë li·ªáu (9 qu·ªπ + total)
        if (parts.length < 9) continue;

        const row = { "Date": dateStr };
        const valueKeys = headers.slice(1);

        valueKeys.forEach((key, index) => {
            let valStr = parts[index];
            if (!valStr || valStr === '-') valStr = "0";
            valStr = valStr.replace(/,/g, '');
            
            let val = 0;
            if (valStr.includes('(') || valStr.includes(')')) {
                val = -Math.abs(parseFloat(valStr.replace(/[()]/g, '')));
            } else {
                val = parseFloat(valStr);
            }
            row[key] = isNaN(val) ? 0 : val;
        });
        rows.push(row);
    }
    // ƒê·∫£o ng∆∞·ª£c ƒë·ªÉ ng√†y m·ªõi nh·∫•t l√™n ƒë·∫ßu
    return rows.reverse();
}

